using System;
using Modele;
using System.Collections.Generic;
using Xunit;

namespace TestsUnitaires
{
    public class UT_Pizza
    {
        [Theory]
        [InlineData("Nom1", "Description1", "Image1", 15)] // Tout bon
        [InlineData("Nom2", "Description2", "Image2", -1)] // Prix négatif
        [InlineData("Nom3", "Description3", "Image3", 0)] // Prix = 0
        [InlineData("", "Description4", "Image4", 15)] // Nom vide
        [InlineData("Nom5", "", "Image5", 15)] // Description vide
        public void TEST_Ctor(string nom, string description, string image, int prix = 12)
        {
            List<Ingredients> liste = new List<Ingredients> { (Ingredients)53, (Ingredients)0 };
            Pizza p = new Pizza(nom, description, image, liste, prix);
            Assert.Equal(nom, p.Nom);
            Assert.Equal(description, p.Description);
            Assert.Equal(image, p.Image);
            Assert.Equal(prix, p.Prix);
            /*Assert.Equal(liste, p.listIngredient);*/
        }
    }
}
